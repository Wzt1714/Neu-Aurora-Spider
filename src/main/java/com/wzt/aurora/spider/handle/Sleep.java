package com.wzt.aurora.spider.handle;

/**
 * <h1>Sleep-用于进行自定义休眠的接口</h1>
 * <p>学校网页的数据在请求过快时会禁止请求，由于各个平台的休眠方式不同，
 * 于是定义了函数式接口用于休眠</p>
 * <p>需要实现休眠指定ms数的方法</p>
 */
@FunctionalInterface
public interface Sleep {
    void sleep(long ms);
}
